public class PaisCompania {
	private String codPais;
	private String codCompania;
	private String desPaisCompa;
	private String codPaisOCR;
	/**
	 * @return the codPais
	 */
	public String getCodPais() {
		return codPais;
	}
	/**
	 * @param codPais the codPais to set
	 */
	public void setCodPais(String codPais) {
		this.codPais = codPais;
	}
	/**
	 * @return the codCompania
	 */
	public String getCodCompania() {
		return codCompania;
	}
	/**
	 * @param codCompania the codCompania to set
	 */
	public void setCodCompania(String codCompania) {
		this.codCompania = codCompania;
	}
	/**
	 * @return the desPaisCompa
	 */
	public String getDesPaisCompa() {
		return desPaisCompa;
	}
	/**
	 * @param desPaisCompa the desPaisCompa to set
	 */
	public void setDesPaisCompa(String desPaisCompa) {
		this.desPaisCompa = desPaisCompa;
	}
	/**
	 * @return the codPaisOCR
	 */
	public String getCodPaisOCR() {
		return codPaisOCR;
	}
	/**
	 * @param codPaisOCR the codPaisOCR to set
	 */
	public void setCodPaisOCR(String codPaisOCR) {
		this.codPaisOCR = codPaisOCR;
	}
	
	
}
