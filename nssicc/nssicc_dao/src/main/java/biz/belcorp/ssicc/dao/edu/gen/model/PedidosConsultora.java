package biz.belcorp.ssicc.dao.edu.gen.model;

public class PedidosConsultora {
	
	private String codigoPais;
	private String codigoEmpresa;
	private String codigoPeriodo;
	private String codigoCliente;
	private String indicadorFacturacion;
	private String indicadorPedido;
	private String fechaRecepcion;
	private String fechafacturacion;
	
	
	public String getCodigoPeriodo() {
		return codigoPeriodo;
	}
	public void setCodigoPeriodo(String codigoPeriodo) {
		this.codigoPeriodo = codigoPeriodo;
	}
	public String getCodigoCliente() {
		return codigoCliente;
	}
	public void setCodigoCliente(String codigoCliente) {
		this.codigoCliente = codigoCliente;
	}
	public String getCodigoEmpresa() {
		return codigoEmpresa;
	}
	public void setCodigoEmpresa(String codigoEmpresa) {
		this.codigoEmpresa = codigoEmpresa;
	}
	public String getCodigoPais() {
		return codigoPais;
	}
	public void setCodigoPais(String codigoPais) {
		this.codigoPais = codigoPais;
	}
	public String getFechafacturacion() {
		return fechafacturacion;
	}
	public void setFechafacturacion(String fechafacturacion) {
		this.fechafacturacion = fechafacturacion;
	}
	public String getFechaRecepcion() {
		return fechaRecepcion;
	}
	public void setFechaRecepcion(String fechaRecepcion) {
		this.fechaRecepcion = fechaRecepcion;
	}
	public String getIndicadorFacturacion() {
		return indicadorFacturacion;
	}
	public void setIndicadorFacturacion(String indicadorFacturacion) {
		this.indicadorFacturacion = indicadorFacturacion;
	}
	public String getIndicadorPedido() {
		return indicadorPedido;
	}
	public void setIndicadorPedido(String indicadorPedido) {
		this.indicadorPedido = indicadorPedido;
	}
	

}
